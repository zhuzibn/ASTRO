function astro_result = astro_build_production_result(config, atomtype_, ...
        mx_init, my_init, mz_init, mmx, mmy, mmz, t, elapsed_seconds, ...
        output_file)
%ASTRO_BUILD_PRODUCTION_RESULT Build the versioned production MAT schema.

schema_version = '1.0';
created_at = char(datetime('now', 'TimeZone', 'local', ...
    'Format', 'yyyy-MM-dd HH:mm:ss Z'));

configuration = config;
configuration.schema_version = schema_version;
configuration.output_file = char(output_file);

atom_distribution = struct( ...
    'atomtype_', atomtype_, ...
    'meaning', '1=RE/Gd, 0=TM/FeCo', ...
    'natomW', configuration.natomW, ...
    'natomL', configuration.natomL, ...
    'compositionn', configuration.compositionn, ...
    'gd_site_count', nnz(atomtype_ == 1), ...
    'tm_site_count', nnz(atomtype_ == 0));

initial_state = struct( ...
    'mx', mx_init, ...
    'my', my_init, ...
    'mz', mz_init);

trajectory = struct( ...
    'mx', mmx, ...
    'my', mmy, ...
    'mz', mmz, ...
    't', t, ...
    'time_unit', 's', ...
    'array_order', '(W,L,time)');

timestamps = struct( ...
    'created_at', created_at, ...
    'trajectory_time_s', t);

provenance = struct( ...
    'entry_point', 'main.m', ...
    'git_commit', astro_command_output('git rev-parse HEAD'), ...
    'git_branch', astro_command_output('git branch --show-current'), ...
    'matlab_version', version, ...
    'operating_system', system_dependent('getos'), ...
    'gpu_info', astro_gpu_info(), ...
    'elapsed_seconds', elapsed_seconds, ...
    'output_file', char(output_file));

astro_result = struct( ...
    'schema_name', 'ASTRO production result', ...
    'schema_version', schema_version, ...
    'configuration', configuration, ...
    'atom_distribution', atom_distribution, ...
    'initial_state', initial_state, ...
    'trajectory', trajectory, ...
    'timestamps', timestamps, ...
    'provenance', provenance);
end

function text = astro_command_output(command)
[status, text] = system(command);
text = strtrim(text);
if status ~= 0 || isempty(text)
    text = 'unavailable';
end
end

function gpu_info = astro_gpu_info()
gpu_info = struct('available', false, 'name', '', ...
    'compute_capability', '', 'driver_version', '', ...
    'toolkit_version', NaN, 'total_memory_bytes', NaN);
try
    selected_gpu = gpuDevice;
    gpu_info.available = true;
    gpu_info.name = selected_gpu.Name;
    gpu_info.compute_capability = selected_gpu.ComputeCapability;
    gpu_info.driver_version = selected_gpu.GraphicsDriverVersion;
    gpu_info.toolkit_version = selected_gpu.ToolkitVersion;
    gpu_info.total_memory_bytes = selected_gpu.TotalMemory;
catch gpu_error
    gpu_info.error = gpu_error.message;
end
end
